-- MOVIES
CREATE TABLE MOVIE (
    ID            INTEGER     UNIQUE NOT NULL,
    NAME          VARCHAR(50) NOT    NULL,
    DURATION      INTEGER     NOT    NULL,
    COST          FLOAT       NOT    NULL,
    INCOME        FLOAT       NOT    NULL,
    RELEASE_DATE  DATE        NOT    NULL,
    GENDER        KEY(GENDERS),
    ACTOR         KEY(ACTORS),
    CLASIFICATION KEY(CLASIFICATIONS),
    COUNTRY       KEY(COUNTRIES),
    STUDIO        KEY(STUDIOS),
    DIRECTOR      KEY(DIRECTORS),
    PRIMARY       KEY(ID)
)
IN MOVIEINDEX;

-- Directors
CREATE TABLE DIRECTORS (
    ID          INTEGER     UNIQUE NOT NULL,
    NAME        VARCHAR(50) NOT    NULL,
    LASTNAME    VARCHAR(30) NOT    NULL,
    NATIONALITY VARCHAR(30) NOT    NULL,
    PRIMARY     KEY(ID)
)
IN MOVIEINDEX;

-- Actor
CREATE TABLE ACTORS (
    ID          INTEGER     UNIQUE NOT NULL,
    NAME        VARCHAR(50) NOT    NULL,
    LASTNAME    VARCHAR(30) NOT    NULL,
    NATIONALITY VARCHAR(30) NOT    NULL,
    PRIMARY     KEY(ID)
)
IN MOVIEINDEX;

-- Actor-Movie
CREATE TABLE ACTORMOVIE (
    ACTORDID  KEY(ACTOR),
    MOVIEID   KEY(MOVIE),
)
IN MOVIEINDEX;

-- Country
CREATE TABLE COUNTRIES (
    ID      INTEGER     UNIQUE NOT NULL,
    NAME    VARCHAR(30) UNIQUE NOT NULL,
    PRIMARY KEY(ID)
)
IN MOVIEINDEX;

-- Gender
CREATE TABLE GENDERS (
    ID      INTEGER     UNIQUE NOT NULL,
    NAME    VARCHAR(30) UNIQUE NOT NULL,
    PRIMARY KEY(ID)
)
IN MOVIEINDEX;

-- Studio
CREATE TABLE STUDIO (
    ID       INTEGER     UNIQUE NOT NULL,
    NAME     VARCHAR(30) UNIQUE NOT NULL,
    LOCATION KEY(COUNTRIES),
    PRIMARY  KEY(ID)
)
IN MOVIEINDEX;
